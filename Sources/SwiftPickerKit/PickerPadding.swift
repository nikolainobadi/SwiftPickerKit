/// An enumeration defining the padding to be used in the `SwiftPicker` selection list.
/// This includes the top and bottom padding values.
enum PickerPadding {
    /// The number of lines to pad at the top of the selection list.
    static let top = 4
    
    /// The number of lines to pad at the bottom of the selection list.
    static let bottom = 2
}
